import sys, pygame
pygame.init()

size = width, height = 320, 240
speed = [2, 2]
black = 0, 0, 0

screen = pygame.display.set_mode(size)

ball = pygame.image.load("fireball.png")        # creates an object , you can use different kinds of image files like png,jpg, bmp etc.
ballrect = ball.get_rect()

while 1:
    for event in pygame.event.get():
        if event.type == pygame.QUIT: sys.exit()

    ballrect = ballrect.move(speed)
    if ballrect.left < 0 or ballrect.right > width:
        speed[0] = -speed[0]
    if ballrect.top < 0 or ballrect.bottom > height:
        speed[1] = -speed[1]

    screen.fill(black)                  # erase the previous image
    screen.blit(ball, ballrect)         # blit is used for drawing the image where ball a source is provided and ballrect is the destination to provide
    pygame.display.flip()